  public void Logar(){
      String sql = "select *  from usuarios where nome=? and senha=?";
      try {
          pst = conexao.prepareStatement(sql);
          pst.setString(1, txtUsuarios.getText());
          pst.setString(2, TextoSenha.getText());
          rs = pst.executeQuery();
          if (rs.next()){
              Tprincipal principal = new Tprincipal();
              principal.setVisible(true);
              this.dispose();
              conexao.close();
          }else{
           JOptionPane.showMessageDialog(null, "O Usuario e/ou senha invalido(s)");
          }
          
      } catch (Exception e) {
          JOptionPane.showMessageDialog(null, e);
      }
  }
  
           


